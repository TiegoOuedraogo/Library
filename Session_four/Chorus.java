public class Chorus {
    public static void main(String[] args) {
        singChorus();
        singChorus();
        singChorus();
        singChorus();
        singChorus();
    }

    public static void singChorus() {
        System.out.println("Don't blame on the sunshine");
        System.out.println("Don't blame on the moonlight");
        System.out.println("Don't blame on good time");
        System.out.println("blame on the boogie\n");
    }
}